package com.beiluoshimen.securityguard.moe;

import android.R.integer;

public class MemInfo {
	public int getCount() {
		return count;
	}
	public void setCount(int count) {
		this.count = count;
	}
	public long getSize() {
		return size;
	}
	public void setSize(long size) {
		this.size = size;
	}
	private int count;
	private long size;
}
